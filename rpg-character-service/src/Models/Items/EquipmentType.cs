namespace RPGCharacterService.Models.Items {
  /// <summary>
  /// Represents the different types of equipment.
  /// </summary>
  public enum EquipmentType {
    /// <summary>
    /// Armor equipment provides protection and affects armor class.
    /// </summary>
    Armor,

    /// <summary>
    /// Weapon equipment is used for attacking and dealing damage.
    /// </summary>
    Weapon,

    /// <summary>
    /// Shield equipment provides additional protection and can be used with one-handed weapons.
    /// </summary>
    Shield
  }
}
